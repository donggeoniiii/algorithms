WITH BIGGEST_FISH_INFO 
AS (
    SELECT 
        FISH_TYPE, MAX(LENGTH) AS LENGTH
    FROM 
        FISH_INFO
    GROUP BY
        1
)

SELECT 
    ID, FISH_NAME, I.LENGTH
FROM 
    FISH_INFO I
JOIN 
    FISH_NAME_INFO N
ON
    I.FISH_TYPE = N.FISH_TYPE
JOIN
    BIGGEST_FISH_INFO B
ON 
    I.FISH_TYPE = B.FISH_TYPE
WHERE
    I.LENGTH = B.LENGTH
ORDER BY
    1;
