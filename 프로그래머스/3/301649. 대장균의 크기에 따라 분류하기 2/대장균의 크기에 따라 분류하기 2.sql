WITH ECOLI_DATA_RANKED AS (
    SELECT 
        ID,
        NTILE(4) OVER (ORDER BY SIZE_OF_COLONY DESC) AS BUCKET
    FROM ECOLI_DATA
)

SELECT ID,
    CASE BUCKET
    WHEN 1 THEN 'CRITICAL'
    WHEN 2 THEN 'HIGH'
    WHEN 3 THEN 'MEDIUM'
    ELSE 'LOW'
    END AS COLONY_NAME
FROM 
    ECOLI_DATA_RANKED
ORDER BY 1 ASC;