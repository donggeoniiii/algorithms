SELECT 
    B.AUTHOR_ID,
    A.AUTHOR_NAME,
    B.CATEGORY,
    SUM (S.SALES * B.PRICE) AS TOTAL_SALES
FROM
    BOOK B
JOIN
    AUTHOR A
ON
    B.AUTHOR_ID = A.AUTHOR_ID
JOIN
    BOOK_SALES S
ON 
    B.BOOK_ID = S.BOOK_ID
WHERE
    S.SALES_DATE >= '2022-01-01' AND S.SALES_DATE <= '2022-01-31'
GROUP BY
    1, 3
ORDER BY
    1 ASC, 3 DESC;


    
    